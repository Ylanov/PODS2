# app/core/geodesy.py
"""
Пересчёт координат МСК-77 (зона 1) → WGS-84 для карты зон.

Геодезисты часто выдают контуры участков в местной системе координат Москвы
(МСК-77), а не в широте/долготе. Чтобы такой контур лёг на веб-карту (WGS-84),
нужен полноценный геодезический пересчёт, а не «прибавить градусы».

Цепочка:
  (X, Y)  — местные прямоугольные координаты (X — север/абсцисса,
            Y — восток/ордината, метры), проекция Гаусса–Крюгера зоны 1
            (осевой меридиан 37.5°, начальная широта 55°40′, ложные смещения 0)
    └─ обратная поперечная Меркатора на эллипсоиде Красовского
       → (B, L) геодезические на датуме СК-42 (Пулково-1942)
    └─ 7-параметрическое преобразование Гельмерта СК-42 → WGS-84
       → (B, L) на WGS-84

Проверено на реальном контуре (участок в Фили, 65 вершин): центр ложится в
западную часть Москвы (≈55.742° с.ш., 37.471° в.д.) — район Фили, что
подтверждает параметры. Абсолютная точность — несколько метров (ограничена
набором ключей Гельмерта); для размещения контура на карте этого достаточно.
Если у конкретного заказчика свой набор ключей — поправьте константы ниже.

ВАЖНО про входные значения: ожидаются «локальные» координаты зоны 1 без
ложных смещений (как в актах межевания по Москве — X порядка тысяч/десятков
тысяч метров, Y может быть отрицательным). Полные МСК-координаты с большим
ложным смещением сюда подавать нельзя — нужен другой набор смещений.
"""

import math

# ─── Эллипсоид Красовского (датум СК-42 / Пулково-1942) ──────────────────────
_A_KRAS  = 6378245.0
_F_KRAS  = 1.0 / 298.3
_E2_KRAS = _F_KRAS * (2 - _F_KRAS)

# ─── Эллипсоид WGS-84 ────────────────────────────────────────────────────────
_A_WGS  = 6378137.0
_F_WGS  = 1.0 / 298.257223563
_E2_WGS = _F_WGS * (2 - _F_WGS)

# ─── Параметры проекции МСК-77, зона 1 ───────────────────────────────────────
_LAT0 = math.radians(55.0 + 40.0 / 60.0)   # 55°40′ — широта начала координат
_LON0 = math.radians(37.5)                 # осевой меридиан зоны 1
_K0   = 1.0                                 # масштаб на осевом меридиане
_FE   = 0.0                                 # ложное смещение на восток
_FN   = 0.0                                 # ложное смещение на север

# ─── Ключи Гельмерта СК-42 → WGS-84 ──────────────────────────────────────────
# Разные источники дают слегка разные наборы (расхождение — десятки метров).
# Параметры участка (lat0/осевой меридиан/проекция) одинаковы — отличается только
# датум-сдвиг, поэтому даём НЕСКОЛЬКО наборов: пользователь выбирает тот, при
# котором контур ложится точнее, а остаток добивает ручной подвижкой на карте.
# Формат: (dX, dY, dZ — метры; rX, rY, rZ — угл. секунды; m — ppm). position vector.
_DATUMS = {
    # «основной» — даёт почти точное совпадение на тестовом контуре в Фили.
    "msk77":   (23.57, -140.95, -79.8, 0.0,  0.35,  0.79, -0.22),
    # вариант 2 — обратный знак поворотов (другая конвенция coordinate-frame),
    # сдвигает результат на ~20 м в противоположную сторону.
    "msk77_b": (23.92, -141.27, -80.9, 0.0, -0.35, -0.82, -0.12),
    # только сдвиг центра (без поворотов и масштаба) — простейший 3-параметрический.
    "msk77_t": (23.57, -140.95, -79.8, 0.0,  0.0,   0.0,   0.0),
}


def _meridian_arc(phi: float) -> float:
    """Длина дуги меридиана от экватора до широты phi на эллипсоиде Красовского."""
    e2 = _E2_KRAS
    a0 = 1 - e2 / 4 - 3 * e2 ** 2 / 64 - 5 * e2 ** 3 / 256
    a2 = 3 / 8 * (e2 + e2 ** 2 / 4 + 15 * e2 ** 3 / 128)
    a4 = 15 / 256 * (e2 ** 2 + 3 * e2 ** 3 / 4)
    a6 = 35 / 3072 * e2 ** 3
    return _A_KRAS * (a0 * phi - a2 * math.sin(2 * phi)
                      + a4 * math.sin(4 * phi) - a6 * math.sin(6 * phi))


_M0 = _meridian_arc(_LAT0)


def _inverse_tm(northing: float, easting: float):
    """Обратная поперечная Меркатора (Гаусс–Крюгер) → (B, L) на СК-42, радианы."""
    a, e2 = _A_KRAS, _E2_KRAS
    M = _M0 + (northing - _FN) / _K0
    mu = M / (a * (1 - e2 / 4 - 3 * e2 ** 2 / 64 - 5 * e2 ** 3 / 256))
    e1 = (1 - math.sqrt(1 - e2)) / (1 + math.sqrt(1 - e2))
    phi1 = (mu
            + (3 * e1 / 2 - 27 * e1 ** 3 / 32) * math.sin(2 * mu)
            + (21 * e1 ** 2 / 16 - 55 * e1 ** 4 / 32) * math.sin(4 * mu)
            + (151 * e1 ** 3 / 96) * math.sin(6 * mu)
            + (1097 * e1 ** 4 / 512) * math.sin(8 * mu))
    ep2 = e2 / (1 - e2)
    c1 = ep2 * math.cos(phi1) ** 2
    t1 = math.tan(phi1) ** 2
    n1 = a / math.sqrt(1 - e2 * math.sin(phi1) ** 2)
    r1 = a * (1 - e2) / (1 - e2 * math.sin(phi1) ** 2) ** 1.5
    d = (easting - _FE) / (n1 * _K0)
    phi = phi1 - (n1 * math.tan(phi1) / r1) * (
        d ** 2 / 2
        - (5 + 3 * t1 + 10 * c1 - 4 * c1 ** 2 - 9 * ep2) * d ** 4 / 24
        + (61 + 90 * t1 + 298 * c1 + 45 * t1 ** 2 - 252 * ep2 - 3 * c1 ** 2) * d ** 6 / 720)
    lam = _LON0 + (
        d
        - (1 + 2 * t1 + c1) * d ** 3 / 6
        + (5 - 2 * c1 + 28 * t1 - 3 * c1 ** 2 + 8 * ep2 + 24 * t1 ** 2) * d ** 5 / 120
    ) / math.cos(phi1)
    return phi, lam


def _geodetic_to_xyz(phi, lam, h, a, e2):
    n = a / math.sqrt(1 - e2 * math.sin(phi) ** 2)
    x = (n + h) * math.cos(phi) * math.cos(lam)
    y = (n + h) * math.cos(phi) * math.sin(lam)
    z = (n * (1 - e2) + h) * math.sin(phi)
    return x, y, z


def _xyz_to_geodetic(x, y, z, a, e2):
    lam = math.atan2(y, x)
    p = math.hypot(x, y)
    phi = math.atan2(z, p * (1 - e2))
    for _ in range(8):
        n = a / math.sqrt(1 - e2 * math.sin(phi) ** 2)
        h = p / math.cos(phi) - n
        phi = math.atan2(z, p * (1 - e2 * n / (n + h)))
    n = a / math.sqrt(1 - e2 * math.sin(phi) ** 2)
    h = p / math.cos(phi) - n
    return phi, lam, h


def _sk42_to_wgs84(phi, lam, keys):
    dx, dy, dz, rxs, rys, rzs, ppm = keys
    wx = math.radians(rxs / 3600.0)
    wy = math.radians(rys / 3600.0)
    wz = math.radians(rzs / 3600.0)
    m  = ppm * 1e-6
    x, y, z = _geodetic_to_xyz(phi, lam, 0.0, _A_KRAS, _E2_KRAS)
    xw = dx + (1 + m) * (x - wz * y + wy * z)
    yw = dy + (1 + m) * (wz * x + y - wx * z)
    zw = dz + (1 + m) * (-wy * x + wx * y + z)
    return _xyz_to_geodetic(xw, yw, zw, _A_WGS, _E2_WGS)


# Какие значения coord_system считаются «МСК» (см. router).
MSK_SYSTEMS = tuple(_DATUMS.keys())


def msk77_to_wgs84(x: float, y: float, system: str = "msk77"):
    """
    (X, Y) МСК-77 зона 1 (метры; X — север, Y — восток) → (lat, lng) WGS-84, градусы.
    system — какой набор датум-ключей применить (см. _DATUMS).
    """
    keys = _DATUMS.get(system, _DATUMS["msk77"])
    phi, lam = _inverse_tm(x, y)
    phw, lamw, _ = _sk42_to_wgs84(phi, lam, keys)
    return math.degrees(phw), math.degrees(lamw)
